/**
 * @brief Главная функция программы.
 *
 * Тестирует функциональность класса Complex, включая арифметические операции,
 * ввод/вывод и операции присваивания.
 *
 * @return Код возврата программы (0 при успешном выполнении).
 */




int main() {
    // Создание комплексных чисел с различными значениями
    Complex a;          ///< Комплексное число с инициализацией по умолчанию
    Complex b(-4);      ///< Комплексное число с действительной частью -4
    Complex c(23.0, 45.9); ///< Комплексное число с действительной и мнимой частями

    // Вывод начальных значений
    std::cout << "Initial values:\n";
    std::cout << "A = " << a << ", B = " << b << ", C = " << c << std::endl;

    // Операция сложения комплексных чисел
    a = b + c;
    std::cout << "A = B + C, A = " << a << std::endl;

    // Операция вычитания комплексных чисел
    Complex m = b - c;
    std::cout << "M = B - C, M = " << m << std::endl;

    // Операции умножения и деления
    std::cout << "M * A = " << m * a << std::endl;
    std::cout << "M / 4.45 = " << m / 4.45 << std::endl;

    // Ввод комплексного числа от пользователя
    Complex d; ///< Комплексное число, вводимое пользователем
    std::cout << "Enter complex number D: ";
    std::cin >> d;

    // Итоговые вычисления с введённым значением
    a += c + d;
    std::cout << "D = " << d << "\nA = " << a << std::endl;

    return 0;
}
